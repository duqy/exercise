#include <iostream>

using namespace std;

int main()
{
    // 定义一个字符数组，用以保存字符串
    char msg[32];
    // 将各个字符依次保存到数组相应的位置上
    msg[0] = 'G';
    msg[1] = 'o';
    msg[2] = 'o';
    msg[3] = 'd';
    msg[4] = '\0';    // 在最后的位置保存一个'\0\,表示字符串结束
    // 输出msg数组中的字符串
    cout<<msg<<endl;   //输出Good
    
    return 0;
}
























 


